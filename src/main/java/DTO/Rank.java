package DTO;

public class Rank {
	
	String m_no;
	String m_name;
	String v_cinfirm;
	String total;
	
	public String getM_no() {
		return m_no;
	}
	public void setM_no(String m_no) {
		this.m_no = m_no;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	public String getV_cinfirm() {
		return v_cinfirm;
	}
	public void setV_cinfirm(String v_cinfirm) {
		this.v_cinfirm = v_cinfirm;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
	
	
}
